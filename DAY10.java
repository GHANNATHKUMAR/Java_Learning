public class DAY10 {
    public static void main(String[] args){
          // Loop is next target 
          // 1. A loop is a programming structure that allows you to repeat a block of code multiple times.
          // 2. There are three types of loops in Java: for loop, while loop, and do-while loop.
            // For Loop
            // A for loop is a control flow statement that allows you to execute a block of code a specific number of times.
            // The syntax of a for loop is:
            // for (initialization; condition; update) {
            //     // code block to be executed
            // }
            // The for loop consists of three parts:
            // 1. Initialization: This is where you initialize a variable that will be used in
            // the loop. This part is executed only once at the beginning of the loop.
            // 2. Condition: This is the condition that is checked before each iteration of the loop. If the condition is true, the code block inside the loop is executed. If the condition is false, the loop is terminated.
            // 3. Update: This is where you update
            // the variable that is used in the loop. This part is executed after each iteration of the loop.
            for (int i = 0; i < 5; i++) {
                System.out.println(i);
            }
            // This will print the numbers 0 to 4, because the loop starts with i = 0 and continues as long as i is less than 5, incrementing i by 1 in each iteration.
            // now next day we will learn about while loop and do-while loop

    }
}
