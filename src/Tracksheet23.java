import java.util.Scanner;

public class Tracksheet23 {
    /* Accept an integer and Print " \Hello World\" n times" */
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        for (int i = 1; i < n; i++) {
            System.out.println("Hello world");
        }
    }


}
