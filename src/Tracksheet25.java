import java.util.Scanner;

public class Tracksheet25 {
    //Print natural number  n to 1.
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        for (int i = n; i >= 1; i--) {
            System.out.print(i + " ");

        }
    }
}
